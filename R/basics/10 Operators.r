# Assignment Operators
a <- 3
b <- 2

# Addition
a + b 

# Substraction
a - b

# Multiplication
a * b

# Division
a / b

# Modulus
a %% b

# Exponent
a ^ b 
#or 
a ** b

# Less than
a < b

# Greater than
a > b

# Less than or equal to
a <= b

# Greater than or equal to
a >= b

# Equal to
a == b

# Not equal
a != b

# If one of the condition is true 
(a > b) | (a < b)
(a > b) || (a < b)

# If both of the condition is true 
(a > b) & (a < b)
(a > b) && (a < b)
